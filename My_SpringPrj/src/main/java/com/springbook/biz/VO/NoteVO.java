package com.springbook.biz.VO;


public class NoteVO{
	int num;
	String title;
	String content;
	String wtime;
	String tosendNickName;
	String userNickName;
	
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getWtime() {
		return wtime;
	}
	public void setWtime(String wtime) {
		this.wtime = wtime;
	}
	public String getTosendNickName() {
		return tosendNickName;
	}
	public void setTosendNickName(String tosendNickName) {
		this.tosendNickName = tosendNickName;
	}
	public String getUserNickName() {
		return userNickName;
	}
	public void setUserNickName(String userNickName) {
		this.userNickName = userNickName;
	}
	
	
}
